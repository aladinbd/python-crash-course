places = ['singapore', 'australia', 'thailand', 'egypt', 'canada']
print("Here is the original list:")
print(places)

print("\nHere is the sorted list")
print(sorted(places))

print("\nHere is the original list again:")
print(places)

print("\nHere is the reverse list:")
places.reverse()
print(places)

print("\nHere is the original list again:")
places.reverse()
print(places)

print("\nHere is the alphabetical order list:")
places.sort()
print(places)

print("\nHere is the reverse order list:")
places.sort(reverse = True)
print(places)
